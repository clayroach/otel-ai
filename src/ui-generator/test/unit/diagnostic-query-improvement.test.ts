        expect(pattern.pattern).toBeDefined()
        expect(pattern.purpose).toBeDefined() 
        expect(pattern.example).toBeDefined()
        expect(pattern.purpose.length).toBeGreaterThan(20)
        expect(pattern.example.length).toBeGreaterThan(30)
